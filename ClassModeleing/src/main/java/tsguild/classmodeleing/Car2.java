package tsguild.classmodeleing;

/**
 *
 * @author jeffc
 */
public class Car2 {
    private String color;
    private String make;
    private double virtualSpeed;
    private int damage;
    private int tireState;

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getMake() {
        return make;
    }

    public void setMake(String make) {
        this.make = make;
    }

    public double getVirtualSpeed() {
        return virtualSpeed;
    }

    public void setVirtualSpeed(double virtualSpeed) {
        this.virtualSpeed = virtualSpeed;
    }

    public int getDamage() {
        return damage;
    }

    public void setDamage(int damage) {
        this.damage = damage;
    }

    public int getTireState() {
        return tireState;
    }

    public void setTireState(int tireState) {
        this.tireState = tireState;
    }
    
    
}
