package StateCapitals2;

/**
 *
 * @author jeffc
 */
public class Capital {
    
    private String capitalName;
    private int capitalPopulation;
    private double capitalSquareMileage;

    
    public String getCapitalName() {
        return capitalName;
    }

    public void setCapitalName(String capitalName) {
        this.capitalName = capitalName;
    }

    public int getCapitalPopulation() {
        return capitalPopulation;
    }

    public void setCapitalPopulation(int capitalPopulation) {
        this.capitalPopulation = capitalPopulation;
    }

    public double getCapitalSquareMileage() {
        return capitalSquareMileage;
    }

    public void setCapitalSquareMileage(double capitalSquareMileage) {
        this.capitalSquareMileage = capitalSquareMileage;
    }
    
}
