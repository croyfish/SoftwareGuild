package SimpleCalculator;

/**
 *
 * @author jeffc
 */
public class Calculator {
    
    public static double addNumbers(double op1, double op2) {
    
        double result = op1 + op2;
        return result;
    }
    
    public static double subtractNumbers(double op1, double op2) {
    
        double result = op1 - op2;
        return result;
    }

    public static double multiplyNumbers(double op1, double op2) {
    
        double result = op1 * op2;
        return result;
    }

    public static double divideNumbers(double op1, double op2) {
    
        double result = op1 / op2;
        return result;
    }    
    
}
